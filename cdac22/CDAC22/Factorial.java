import java.util.*;
class Factorial
{ public static void main(String[]args)
  { 
    Scanner dh= new Scanner(System.in);
	System.out.println("Enter the no. ");
	double i= dh.nextDouble();
	//int j=i-1;
	 for(double j=i-1 ; j>=1; j--)
	 {
	  i=j*i;
	 }
	  System.out.println("Factorial of given no"+i);
	  
	}
}	